	public String[] find(String ocupation){
		String key = null;
		if ( occupationList.containsValue(ocupation)){
			
			for (Entry entry : occupationList.entrySet()) {
		        if (ocupation.equals(entry.getValue())) {
		            key = (String) entry.getKey();
		        }
		    }
		
			//  
			cat[0] = occupationList.get(key.substring(0, 2) + "-0000");
			cat[1] = occupationList.get(key.substring(0, 4) + "000");
			cat[2] = occupationList.get(key.substring(0, 6) + "0");
			if (!key.substring(6).equals("0")){
				cat[3] = ocupation;
			} 
		
		}
		//
		return cat;
	}
	


	

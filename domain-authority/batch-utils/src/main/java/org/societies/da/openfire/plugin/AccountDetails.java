package org.societies.da.openfire.plugin;

public class AccountDetails {

	private String username;
	private String password;
	private String name;
	private String email;
	
	
	public AccountDetails() {}
	public AccountDetails(String username, String password, String name, String email) {
		this.username = username;
		this.password = password;
		this.name = name;
		this.email = email;
	}
	
	
	public AccountDetails(String line) {
		// parsing constructor
		String[] tokens = line.split(",");
		username = tokens[0];
		if (tokens.length>1) {
			password = tokens[1];
			if (tokens.length>2) {
				name = tokens[2];
				if (tokens.length>3) {
					email = tokens[3];
				}
			}
		}
	}
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((email == null) ? 0 : email.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result
				+ ((password == null) ? 0 : password.hashCode());
		result = prime * result
				+ ((username == null) ? 0 : username.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AccountDetails other = (AccountDetails) obj;
		if (email == null) {
			if (other.email != null)
				return false;
		} else if (!email.equals(other.email))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (password == null) {
			if (other.password != null)
				return false;
		} else if (!password.equals(other.password))
			return false;
		if (username == null) {
			if (other.username != null)
				return false;
		} else if (!username.equals(other.username))
			return false;
		return true;
	}
	
	public String getUrlParameters() {
		StringBuilder sb = new StringBuilder("username="+username);
		if (password!=null)
			sb.append("&password="+password);
		if (name!=null)
			sb.append("&name="+name);
		if (email!=null)
			sb.append("&email="+email);
		return sb.toString();
	}
}
